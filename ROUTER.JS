const path = require('path');
const fs = require('fs');

// Lazy-loaded modules
let musicService = null;
let brainService = null;
let stateDb = null;

function getMusic() {
  if (!musicService) musicService = require('./MUSIC/netease');
  return musicService;
}

function getBrain() {
  if (!brainService) brainService = require('./BRAIN/claude');
  return brainService;
}

function getDb() {
  if (!stateDb) stateDb = require('./STATE.DB.js');
  return stateDb;
}

// в”Җв”Җв”Җ Shared Helpers в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

async function searchWithFallback(style, count) {
  const music = getMusic();
  // Try full style keyword first
  let tracks = await music.search(style, count);
  if (tracks && tracks.length > 0) return tracks;

  // Fallback: split into simpler keywords and try each
  const parts = style.split(/[\s,пјҢ/гҖҒ]+/).filter(p => p.length >= 2 && !/^[a-z]$/i.test(p));
  for (const part of parts.slice(0, 4)) {
    tracks = await music.search(part, count);
    if (tracks && tracks.length > 0) return tracks;
  }
  return [];
}

function readTasteHint() {
  const tastePath = path.join(__dirname, 'USER', 'taste.md');
  try {
    const raw = fs.readFileSync(tastePath, 'utf-8');
    const match = raw.match(/еҒҸеҘҪйЈҺж ј\n([\s\S]*?)(?=\n##)/);
    if (match) return match[1].trim();
    return raw.slice(0, 300);
  } catch (_) { return ''; }
}

// в”Җв”Җв”Җ Intent Classification в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

const INTENTS = {
  PLAY: 'play',
  RECOMMEND: 'recommend',
  CHAT: 'chat',
  QUERY: 'query',
};

function classifyIntent(text) {
  const t = text.toLowerCase().trim();

  // Check recommend first (higher priority than vague "еҗ¬")
  const recommendPatterns = ['жҺЁиҚҗ', 'жңүд»Җд№ҲеҘҪеҗ¬зҡ„', 'жқҘзӮ№', 'йҡҸдҫҝж”ҫ', 'suggest'];
  for (const p of recommendPatterns) {
    if (t.includes(p)) return INTENTS.RECOMMEND;
  }

  // Play: "еҗ¬" only when it's the main verb, not "йҖӮеҗҲXXеҗ¬зҡ„жӯҢ"
  const playPatterns = ['жҲ‘жғіеҗ¬', 'жғіеҗ¬', 'ж’ӯж”ҫ', 'ж”ҫдёҖйҰ–', 'жқҘдёҖйҰ–', 'жқҘйҰ–', 'зӮ№жӯҢ', 'дёӢдёҖйҰ–', 'жҚўдёҖйҰ–', 'play'];
  for (const p of playPatterns) {
    if (t.includes(p)) return INTENTS.PLAY;
  }
  // Bare "еҗ¬" вҖ” match only if not part of "еҗ¬зҡ„" relative clause
  // "жҲ‘жғіеҗ¬е‘Ёжқ°дјҰ" вҶ’ PLAY  /  "йҖӮеҗҲи·‘жӯҘеҗ¬зҡ„жӯҢ" вҶ’ skip (caught by recommend or falls to CHAT)
  if (t.includes('еҗ¬') && !t.includes('еҗ¬зҡ„')) return INTENTS.PLAY;

  // Simple queries that don't need AI
  const queryPatterns = ['еҮ зӮ№', 'ж—Ҙжңҹ', 'д»ҠеӨ©еӨ©ж°”', 'ж—¶й—ҙ'];
  for (const p of queryPatterns) {
    if (t.includes(p)) return INTENTS.QUERY;
  }

  return INTENTS.CHAT;
}

// в”Җв”Җв”Җ Quantity Extraction в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

function extractCount(message) {
  const t = message || '';
  // Chinese numerals: дёҖвҶ’1, дёӨвҶ’2, дёүвҶ’3, еӣӣвҶ’4, дә”вҶ’5, е…ӯвҶ’6, дёғвҶ’7, е…«вҶ’8, д№қвҶ’9, еҚҒвҶ’10
  const chineseDigits = { 'дёҖ':1,'дёӨ':2,'дёү':3,'еӣӣ':4,'дә”':5,'е…ӯ':6,'дёғ':7,'е…«':8,'д№қ':9,'еҚҒ':10 };
  // Pattern: "NйҰ–" / "NдёӘ" / "NйҰ–жӯҢ" (both Arabic and Chinese)
  const m1 = t.match(/(\d+)\s*[йҰ–дёӘ]/);
  if (m1) return Math.min(parseInt(m1[1]), 20);
  const m2 = t.match(/([дёҖдёӨдёүеӣӣдә”е…ӯдёғе…«д№қеҚҒ])\s*[йҰ–дёӘ]/);
  if (m2) return Math.min(chineseDigits[m2[1]] || 5, 20);
  // "еҮ йҰ–" / "жқҘеҮ йҰ–" вҶ’ default 5
  if (/еҮ [йҰ–дёӘ]/.test(t)) return 5;
  // "еҚҒйҰ–" (bare еҚҒ)
  if (/еҚҒ[йҰ–дёӘ]/.test(t)) return 10;
  // "жқҘзӮ№" / "жҺЁиҚҗ" without number вҶ’ default 5
  return 5;
}

// в”Җв”Җв”Җ Chat Handler в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

async function handleChat(body) {
  const { message, sessionId } = body;
  const db = getDb();

  let sid = sessionId || db.createSession();
  db.saveMessage(sid, 'user', message);

  const intent = classifyIntent(message);
  const count = extractCount(message);

  switch (intent) {
    case INTENTS.PLAY:
      return handlePlayIntent(message, sid);

    case INTENTS.RECOMMEND:
      return handleRecommendWithFallback(message, sid, count);

    case INTENTS.QUERY:
      return handleQueryIntent(message, sid);

    default:
      return handleAIChat(message, sid, count);
  }
}

// в”Җв”Җв”Җ Intent Handlers в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

async function handlePlayIntent(message, sessionId) {
  const keyword = message.replace(/ж’ӯж”ҫ|ж”ҫдёҖйҰ–|жқҘдёҖйҰ–|жқҘйҰ–|жҲ‘жғіеҗ¬|жғіеҗ¬|еҗ¬|зӮ№жӯҢ/g, '').trim() || 'жҺЁиҚҗ';
  const track = await resolveTrack(keyword);

  if (track) {
    return {
      reply: `еҘҪзҡ„пјҢдёәдҪ ж’ӯж”ҫ ${track.ar?.[0]?.name || ''} зҡ„гҖҠ${track.name}гҖӢ`,
      action: 'play',
      data: { track },
    };
  }
  return { reply: 'жҠұжӯүпјҢжІЎжңүжүҫеҲ°иҝҷйҰ–жӯҢгҖӮиҜ•иҜ•жҚўдёӘе…ій”®иҜҚпјҹ', action: 'none' };
}

async function handleRecommendWithFallback(message, sessionId, count = 5) {
  // Try AI first вҖ” it gives more personalized recommendations
  try {
    const brain = getBrain();
    const db = getDb();
    const history = db.getMessages(sessionId, 10);
    const result = await brain.chat(message, history);
    if (result && result.message) {
      db.saveMessage(sessionId, 'assistant', result.message);

      // If AI wants to recommend, search for matching tracks
      if (result.action === 'recommend' && result.data?.style) {
        const music = getMusic();
        let tracks = await searchWithFallback(result.data.style, count);
        if (tracks && tracks.length > 0) {
          // Resolve ALL tracks' URLs in parallel
          await Promise.all(tracks.map(async (t) => {
            const url = await music.getTrackUrl(t.id);
            if (url) t.url = url;
          }));
          result.data.tracks = tracks;
          result.data.track = tracks[0];  // first track for immediate playback
        }
      }
      // If AI wants to play track(s), resolve them
      if (result.action === 'play') {
        if (result.data?.trackNames && result.data.trackNames.length > 1) {
          // Multiple tracks вҖ” resolve all in parallel
          const resolved = await Promise.all(
            result.data.trackNames.map(tn => {
              const kw = tn.artist ? `${tn.trackName} ${tn.artist}` : tn.trackName;
              return resolveTrack(kw);
            })
          );
          const valid = resolved.filter(Boolean);
          if (valid.length > 0) {
            result.data.tracks = valid;
            result.data.track = valid[0];
          }
        } else if (result.data?.trackName) {
          // Single track вҖ” legacy path
          const keyword = result.data.artist
            ? `${result.data.trackName} ${result.data.artist}`
            : result.data.trackName;
          const track = await resolveTrack(keyword);
          if (track) result.data.track = track;
        }
      }

      return { reply: result.message, action: result.action, data: result.data };
    }
  } catch (err) {
    console.error('[router] AI recommend failed, falling back to local:', err.message);
  }

  // Fallback to local recommendation (no AI needed)
  return handleRecommendIntent(message, sessionId, count);
}

async function handleRecommendIntent(message, sessionId, count = 5) {
  const music = getMusic();
  const db = getDb();
  const recent = db.recentlyPlayed();
  const tasteHint = readTasteHint();

  const tracks = await music.recommend(tasteHint);

  if (tracks && tracks.length > 0) {
    // Take requested count, avoid recently played
    const picks = tracks.filter(t => !recent.includes(String(t.id))).slice(0, count);
    const selected = picks.length > 0 ? picks : tracks.slice(0, count);

    // Resolve ALL selected tracks' URLs
    await Promise.all(selected.map(async (t) => {
      const url = await music.getTrackUrl(t.id);
      if (url) t.url = url;
    }));

    // Build a nice reply with track list
    const list = selected.map((t, i) =>
      `${i + 1}. ${t.ar?.[0]?.name || ''} вҖ” гҖҠ${t.name}гҖӢ`
    ).join('\n');

    return {
      reply: `дёәдҪ еҮҶеӨҮдәҶ ${selected.length} йҰ–пјҢй©¬дёҠејҖе§Ӣж’ӯж”ҫпјҡ\n${list}`,
      action: 'recommend',
      data: { track: selected[0], tracks: selected },
    };
  }
  return { reply: 'жҡӮж—¶жІЎжңүж–°зҡ„жҺЁиҚҗпјҢиҜ•иҜ•жҗңзҙўзү№е®ҡжӯҢжӣІпјҹ', action: 'none' };
}

async function handleQueryIntent(message, sessionId) {
  const now = new Date();
  const dateStr = now.toLocaleDateString('zh-CN', {
    year: 'numeric', month: 'long', day: 'numeric', weekday: 'long',
  });
  return {
    reply: `зҺ°еңЁжҳҜ ${dateStr}пјҢжңүд»Җд№ҲжҲ‘еҸҜд»Ҙеё®дҪ зҡ„пјҹ`,
    action: 'none',
  };
}

